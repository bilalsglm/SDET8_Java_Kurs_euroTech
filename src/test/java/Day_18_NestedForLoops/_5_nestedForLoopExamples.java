package Day_18_NestedForLoops;

public class _5_nestedForLoopExamples {
    public static void main(String[] args) {

         for(int i=1; i<=7; i++){

             for(int j=1; j<=i; j++){
                 System.out.print(" * ");
             }
             System.out.println();

         }


    }
}
