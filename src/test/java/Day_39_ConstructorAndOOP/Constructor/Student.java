package Day_39_ConstructorAndOOP.Constructor;

public class Student {
    int id;
    String name;
    String surname;
    int classRoom;

    public Student(){

    }
    public Student(int id,String name,String surname,int classRoom){
        this.id=id;
        this.surname=surname;
        this.name=name;
        this.classRoom=classRoom;

    }

}
