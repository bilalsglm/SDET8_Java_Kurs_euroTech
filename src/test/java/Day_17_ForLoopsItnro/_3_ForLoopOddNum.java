package Day_17_ForLoopsItnro;

public class _3_ForLoopOddNum {
    public static void main(String[] args) {

        // 5 ten 240 a kadar tek sayilarin yazdiriniz

        for( int i=5; i<=140; i++){
            if (i%2!=0){
                System.out.print(i +" ");
            }

        }

    }
}
