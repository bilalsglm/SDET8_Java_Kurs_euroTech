package Day_17_ForLoopsItnro;

public class _1_ForLoopsIntro {
    public static void main(String[] args) {

        for (int i=0; i<=50; i+=4){
            System.out.println("i = " + i);
        }
        for (int i=50; i>=0; i-=4){
            System.out.println("i = " + i);
        }



    }
}
